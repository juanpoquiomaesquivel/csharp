// C# Keywords and Identifiers
using System;

namespace HelloWorld // defining a namespace
{
    class Hello // defining a class
    {
        static void Main(string[] args) // main method of the program
        {
            long number = 45; // 'long' is a keyword, 'number' is an identifier
            Console.WriteLine("value(number): " + number);
        }
    }
}
